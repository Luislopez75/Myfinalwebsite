myitem1.addEventListener("mouseover", onMouseOver);
myitem1.addEventListener("mouseout", onMouseOut);

function onMouseOver() {
    myitem1.style.color = "aqua";
}

function onMouseOut() {
    myitem1.style.color = "";
}


