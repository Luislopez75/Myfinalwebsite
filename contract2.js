myitem6.addEventListener("mouseover", onMouseOver6);
myitem6.addEventListener("mouseout", onMouseOut6);

function onMouseOver6() {
    myitem6.style.color = "aqua";
}

function onMouseOut6() {
    myitem6.style.color = "";
}


